class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
         sort(a.begin(),a.end());
         long long int i,j=m-1,ans=INT_MAX;
         for(i=0;i<=n-m;i++,j++){
             ans=min(ans,a[j]-a[i]);
         }
          return ans;
    }   
};

